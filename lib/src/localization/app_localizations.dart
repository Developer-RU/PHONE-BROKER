  String t(String key) {
    final lang = _localizedValues[locale.languageCode];
    return lang?[key] ?? _localizedValues['en']![key] ?? key;
  }

  String importedConfigs(int count) =>
      t('importedConfigs').replaceAll('{count}', '$count');

  String deletePrompt(String name, int port) {
    return switch (locale.languageCode) {
      'ru' => 'Удалить $name на порту $port?',
      'es' => '¿Eliminar $name en el puerto $port?',
      'fr' => 'Supprimer $name sur le port $port ?',
      'de' => '$name auf Port $port loschen?',
      'zh' => '删除 $name（端口 $port）？',
      'ar' => 'حذف $name على المنفذ $port؟',
      _ => 'Delete $name on port $port?',
    };
  }

  String minutes(int value) {
    return switch (locale.languageCode) {
      'ru' => '$value мин',
      'es' => '$value min',
      'fr' => '$value min',
      'de' => '$value Min',
      'zh' => '$value 分钟',
      'ar' => '$value دقيقة',
      _ => value == 1 ? '1 minute' : '$value minutes',
    };
  }

  String uptime(Duration uptime) {
    final hours = uptime.inHours;
    final minutes = uptime.inMinutes.remainder(60);
    final seconds = uptime.inSeconds.remainder(60);
    return switch (locale.languageCode) {
      'ru' => '$hours ч $minutes м $seconds с',
      'es' => '$hours h $minutes m $seconds s',
      'fr' => '$hours h $minutes m $seconds s',
      'de' => '$hours h $minutes m $seconds s',
      'zh' => '$hours 时 $minutes 分 $seconds 秒',
      'ar' => '$hours س $minutes د $seconds ث',
      _ => '$hours h $minutes m $seconds s',
    };
  }

  String languageName(String code) {
    return switch (code) {
      'system' => t('languageSystem'),
      'en' => t('langEn'),
      'ru' => t('langRu'),
      'es' => t('langEs'),
      'fr' => t('langFr'),
      'de' => t('langDe'),
      'zh' => t('langZh'),
      'ar' => t('langAr'),
      _ => t('langEn'),
    };
  }

  /// Returns language name in its native language (not translated to current locale)
