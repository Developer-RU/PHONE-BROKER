    return switch (code) {
      'system' => t('languageSystem'),
      'en' => 'English',
      'ru' => 'Русский',
      'es' => 'Español',
      'fr' => 'Français',
      'de' => 'Deutsch',
      'zh' => '中文',
      'ar' => 'العربية',
      _ => 'English',
    };
  }

  String statusLabel(BrokerStatus status) {
    return switch (status) {
      BrokerStatus.running => t('statusRunning'),
      BrokerStatus.stopped => t('statusStopped'),
      BrokerStatus.starting => t('statusStarting'),
