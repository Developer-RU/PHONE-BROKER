      NetworkMode.offline => t('modeOffline'),
      NetworkMode.wifi => t('modeWifi'),
      NetworkMode.hotspot => t('modeHotspot'),
      NetworkMode.cellular => t('modeCellular'),
      NetworkMode.unknown => t('modeUnknown'),
